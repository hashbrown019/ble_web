def heuristic(a, b):
	return math.sqrt((a[0] - b[0]) ** 2 + (a[1] - b[1]) ** 2)
	
def get_neighbors(point,matrix):
	neighbors = []
	m = len(matrix) 
	n = len(matrix[0]) 
	x, y = point 
	for i in range(x - 1, x + 2): 
		for j in range(y - 1, y + 2): 
			if (0 <= i < m) and (0 <= j < n): 
				neighbors.append((i, j)) 
	return neighbors

